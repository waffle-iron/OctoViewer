import UIKit

extension UIColor {
  class var octoGreen: UIColor {
    return UIColor(red: 67.0 / 255.0, green: 139.0 / 255.0, blue: 25.0 / 255.0, alpha: 1.0)
  }

  class var octoSlate: UIColor {
    return UIColor(red: 36.0 / 255.0, green: 41.0 / 255.0, blue: 46.0 / 255.0, alpha: 1.0)
  }

  class var octoSkyBlue: UIColor {
    return UIColor(red: 0.0, green: 121.0 / 255.0, blue: 145.0 / 255.0, alpha: 1.0)
  }

  class var octoNightBlue: UIColor {
    return UIColor(red: 34.0 / 255.0, green: 46.0 / 255.0, blue: 80.0 / 255.0, alpha: 1.0)
  }

  class var octoShadowBlue: UIColor {
    return UIColor(red: 117.0 / 255.0, green: 142.0 / 255.0, blue: 164.0 / 255.0, alpha: 1.0)
  }
}
